int		ft_iterative_factorial(int nb)
{
	int a;
	int b;

	if (nb == 1)
	{
		return (nb);
	}
	else if (nb < 1)
	{
		return (0);
	}
	else
	{
		a = 1;
		b = 1;
		while (b <= nb)
		{
			a = a * b;
			b++;
		}
		return (a);
	}
}
