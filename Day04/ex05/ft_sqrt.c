int		ft_sqrt(int nb)
{
	int i;
	int result;

	i = 1;
	result = 1;
	if (nb == 0 || nb == 1)
	{
		return (nb);
	}
	else
	{
		while (result <= nb)
		{
			i++;
			result = i * i;
		}
	}
	return (0);
}
