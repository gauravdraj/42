void	ft_print_comb(void)\
{
	int i;
	int j;
	int k;

while (i <= 7)
	{	
		while (j <= 8)
		{
			while (k <= 9)
			{
				putchar(i+"0");
				putchar(j+"0");
				putchar(k+"");
				k++;
			}
			i++;
		}
		j++;
	}
}

int main()
{
	ft_print_comb();
	return (0);
}
